# Leio o primeiro valor inteiro
valor1 = int(input("Digite o primeiro valor inteiro: "))

# Leio o segundo valor inteiro
valor2 = int(input("Digite o segundo valor inteiro: "))

# Calculo o produto entre os dois valores e armazeno na variável PROD
PROD = valor1 * valor2

# Exibo o resultado no formato solicitado, com espaços ao redor do sinal de igualdade
print(f"PROD = {PROD}")